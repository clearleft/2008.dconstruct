		<h4 id="workshop1">Managing Community, by Design</h4>
		<p>If there&#8217;s one thing we&#8217;ve learned from the rise of the social Web, it&#8217;s that all sites have communities whether they realize it or not. Sites that do realize this, however, tend to do a whole lot better than sites that don&#8217;t. The success (or failure) of your site has everything to do with how and why the community around it forms.</p>

		<p>Fortunately, we&#8217;ve learned a lot about the mechanics of how and why community happens, which go well beyond just the interaction and visual design of your site. In this workshop, Leslie, Lane, and Thor, who have years of experience at the intersection of user experience, design, and community, will walk you through examples and exercises designed to answer the questions you need to ask about organizational goals, community management style, internal team structure, and the design choices you need to make in order to bring all those together.</p>

		<p>Some of what we&#8217;ll cover will include:</p>

		<ul class="bullets">
			<li>Mapping community design to organizational vision</li>
			<li>Different types of online communities, and which one is right for your organization</li>
			<li>How interaction and visual design affect community expectation and behavior</li>
			<li>Structuring your design and community teams</li>
			<li>Using design to deal with trolls, griefers, and other permanently unhappy individuals</li>
			<li>Connecting your community to the rest of the Web</li>
		</ul>
		
