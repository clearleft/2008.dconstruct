		<h4 id="workshop3">Scaling your Design with your Community</h4>

		<p>Scaling is of particular importance in social sites where your community can grow (and contract) rapidly. As your site expands, you&#8217;re going to face many issues you might not have encountered before. Your design will need to handle increased content input by users; to adjust to demands of an ever-growing and demanding user-base; and to technically manage site performance. Daniel and Mark each have experience developing sites that have successfully scaled over time and will be able to knowledgeably discuss handling massive community input, adapting your design over time, using technical tricks to increase performance, and enabling both new and existing users to participate as communities evolve.</p>

		<p>This workshop aims to cover the following discussion points</p>
		
		<ul class="bullets">
			<li>Technical performance tips (sprites, css management, coding standards, etc)</li>
			<li>Developing levels of participation on your site... advanced users vs novices</li>
			<li>Issues of negative and positive participation (particularly how this evolves as sites scale)</li>
			<li>Adapting for increasing levels of input. (Digg&#8217;s story submission process used to have 100 stories per day, now 16,000)</li>
			<li>Handling feedback from your users (particularly how this evolves as sites scale)</li>
			<li>Avoiding feature-bloat in your design.</li>
		</ul>
		
		<p><strong>Led by:</strong> Daniel Burka and Mark Trammell</p>
