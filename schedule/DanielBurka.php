		<h3 class="summary">Designing for Interaction</h3>
		<p>Creating a social site sounds great until you get around to actually designing that ambiguous &#8216;social&#8217; part that&#8217;s central to its success. Enabling and encouraging your community to participate is a complex challenge that only gets more sophisticated as the populace on your site grows. In many critical areas, you&#8217;ll come up against the curious juxtapositions of designing social interactions. Encouraging positive activities while discouraging negative behaviors, satisfying power users while catering to lurkers, ensuring privacy while fostering openness, and creating pathways while remaining open to unexpected developments, are just some of the hurdles you&#8217;re likely to face as you design your site.</p>
