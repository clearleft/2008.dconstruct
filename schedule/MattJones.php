		<h3 class="summary">Designing for the Coral Reef</h3>
		<p>Can you build a successful website that nobody ever has to visit?</p>

		<p>Feeds, APIs, widgets, Facebook apps, mobile and instant messaging mean that there are many ways for users to interact with a service without them having to visit the main website. When we first talked about building Dopplr, we wanted give users more choice about how they get their information into and out of the application. In this talk, we&#8217;ll describe how the site at dopplr.com is just one manifestation of a many-headed Internet service. We&#8217;ll talk about how this affects the user interface design and the data modeling, and how it strengthens the relationship between designer and developer.</p>
		
