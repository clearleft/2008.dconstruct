		<h4 id="workshop4">Social Design: from Strategy to Interface</h4>

		<p>Have you decided to leverage the social interaction of your audience to provide more and better value but aren&#8217;t sure where to start or how to move forward? In this workshop, Joshua Porter will show you how to outline a social design strategy and then translate that strategy directly into design.</p>

		<p>This hands-on workshop will be organized around several exercises:</p>

		<ul class="bullets">
			<li>Mapping out a social design strategy</li>
			<li>Identifying Core Activities, Objects, and Product Features</li>
			<li>Creating Interfaces/prioritizing calls to action </li>
			<li>Leveraging social principles in design</li>
		</ul>

		<p>During the workshop teams will create actual screen designs from scratch. Even if you&#8217;re not an interface designer, this workshop will help you get your team on the same page with the same priorities.</p>

		<p>During the day, Josh will share stories from Facebook, Digg, Amazon and others, showing how their interfaces have evolved over time according to lessons they&#8217;ve learned. He&#8217;ll help you avoid the mistakes that have plagued the early innovators in social design. He'll also describe core psychological research studies that can help point the way toward better social design.</p>

		<p>After attending this workshop, you&#8217;ll be able to articulate a social design strategy and then design interfaces that directly support it.</p>
		
